package data_types;

public class All_Data_type {

	public static void main(String[] args) {

		byte b=127;																//3
		System.out.println("Byte range upto = "+b);
		
		short s=32767;
		System.out.println("Short range upto = "+s);		//5
		
		int i=1276823145;
		System.out.println("Int range upto = "+i);

	}

}
